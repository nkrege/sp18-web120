<p>There are a couple of things you will want to look for when choosing an eCommerce solution:</p>
<ul>
    <li>Ease of Use</li>
    <li>Unlimited Products, Bandwidth, and storage</li>
    <li>Complex discount features</li>
    <li>Email marketing features</li>
    <li>Inventory Management Features</li>
    <li>Shipping Options</li>
    <li>Tax Calculator</li>
    <li>International language and currency support</li>
    <li>Reporting tools</li>
    <li>SEO tools</li>
</ul>

<p>So who do you go with? Well, looking at several options, I will say Zoey for SaaS and Magento for open source.</p>

<p>So why Zoey? I like their easy to use drag and drop User Interface(UI).It allows someone, who isn’t very technically experiences, to move objects around a page, change colors and fonts, without needing a ton of knowledge. They also offer a rather robust support service that will make troubleshooting a breeze. A small drawback, however, is the cost. Zoey can be a bit pricier than some of the other options out there.</p>

<p>As for Magento, Developer skills are required and there is a steep learning curve. Because this is open source, there is no customer supported offered. So, be prepared to Google if you can’t figure something out. On the upside, this is a free download that is highly customizable with a strong user community that will cut down on your Googling if you get lost. Now, if you like Magento, and decide that maybe it’s too technical for you, they do offer an SaaS option as well. They call it Magento Commerce. This gives you a lot more access to tools and support, but there will be a cost involved.</p>


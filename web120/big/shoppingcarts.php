<p>Shopping Cart Web Applications, or eCommerce solutions/platforms, allow you to build an online store. There are typically two different ways you can get an eCommerce solution, via SaaS and open source. SaaS, or Software as a Service, either lets you build a full site, or plug a store tab into your existing website. SaaS is typically billed as a monthly service and offers more support, security and design tools. This is often recommended for someone who does not have much technical experience.</p>

<p>Open source, on the other hand, are downloadable and free to implement. This is recommended for someone who has more technical experience. There is little to no support offered, and it is up to the merchant to set everything up from the ground up. The benefit to open source is the little to no cost, and customizability.</p>
